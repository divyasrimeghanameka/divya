import java.util.*;
class Narrow
{
  public static void main(String args[])
  { 
     double d=10.69;
     System.out.println("double value is: "+d);
     int num=(int)d;
     System.out.println("integer value is: "+num);
     byte b=(byte)num;
     System.out.println("byte value is: "+b);
     
         }
}
   
